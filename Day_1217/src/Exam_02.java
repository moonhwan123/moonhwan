/*
[자료형 - DateType(기억장소의 크기와 연관)]

1. 기본자료형
	문자 : char(2) -> '정수타입'도 가능 함 시험에서 주의!
	정수 : byte(1) , int(2) , long(8) ,short(2)
	실수 : double, float
	논리 : boolean(true/false)

	*자바에는 문자열 자료형은 없음 원래 
	*char도 정수 -> 결국 숫자/논리로 분리가능

2. 참조자료형 (주소/위치)
	배열 
	클래스,인터페이스 
	
	

-> 이런건 면접에서 물어 볼 수도 있음!

 */


public class Exam_02 {
	
	public static void main(String[] args) {
		
		char ch ; // 2byte 
		int jung; // 4byte
		double dou; // 8byte
		boolean bool; // 1byte
		 
		ch = 's'; // = -> 대입 연산자
		jung = 123791; // 정수대입
		dou = 3.14; // 실수대입
		bool = true; // true대입
		
		
		System.out.println("char ch = " + ch);
		System.out.println("int jung = " + jung);
		System.out.println("double dou = " + dou);
		System.out.println("boolean bool = " + bool);
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	
}

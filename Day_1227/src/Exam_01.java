/*


/ : 나누기 연산자 ( 정수와 정수의 연산은 정수 , 나머지는 실수)


 */
public class Exam_01 {
	public static void main(String[] args) {
		
		
		int a = 5;
		System.out.println("a = " + a++);
		System.out.println("a = " + a--);
		System.out.println("a = " + -a);
		System.out.println("a = " + ~a); // -붙여서 1
		System.out.println("a = " + (float)a);
		System.out.println("a = " + (a>3));
		System.out.println("a = " + !(a>3));
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}


public class Exam_05 {
	
	public static void main(String[] args) {
		
		int digit = 10;
		int oct = 010; // 8진수 10
		int hex = 0x10; // 16진수 10
		int bin = 0b10; // 2진수 10
		
		System.out.println("10진수 = "+digit);
		System.out.println("8진수 = "+oct);
		System.out.println("16진수 = "+hex);
		System.out.println("2진수 = "+bin);
	}

}
